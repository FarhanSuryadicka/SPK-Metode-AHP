    <h2>Penilaian Pengangkatan Karyawan Tetap</h2>
    Sistem ini membantu pengambil keputusan dalam menyeleksi serta menilai kandidat karyawan yang akan diangkat menjadi karyawan tetap yayasan dengan membandingkan calon karyawan tetap pada setiap kriteria,
    metode yang digunakan adalah AHP (<i>Analytical Hierarchy Process</i>).<br /><br />

    <b>Proses yang Anda harus lakukan :</b>
    <ol>
        <li>Masukkan Kriteria yang dibutuhkan.</li>
        <li>Masukkan Sub Kriteria dari setiap kriteria yang dibutuhkan.</li>
        <li>Masukkan Alternatif/kandidat karyawan.</li>
        <li>Mulai seleksi dengan metode AHP :
            <ul>
                <li>Masuk ke Nilai Bobot Kriteria dan isikan nilai perbandingan kriteria untuk menentukan bobot kriteria.</li>
                <li>Masuk ke Nilai Bobot SubKriteria dan isikan nilai perbandingannya.</li>
                <li>Masuk ke Nilai Bobot Alternatif dan isikan bobot alternatifnya.</li>
                <li>Masuk ke Perhitungan untuk melihat hasil perhitungan dan perangkingan.</li>
            </ul>
        </li>
        <li>Anda juga dapat mencetak data Kriteria, Sub Kriteria, Alternatif dan Rangking.</li>
    </ol>
</div>